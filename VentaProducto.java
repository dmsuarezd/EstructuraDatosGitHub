    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // Instanciaci√≥n de la tabla
        int SubtotalProducto;
        DefaultTableModel modelo = (DefaultTableModel) tblRegistro.getModel();
        
        // Validaciones
        // Producto
        if(txtProducto.getText().equals("")){
            JOptionPane.showMessageDialog(null, "*Campo Requerido [Producto] -> Debe ingresar el Producto");
            txtProducto.requestFocus();
            return;        
        }
        
        // Cantidad
        if(txtCantidad.getText().equals("")){
            JOptionPane.showMessageDialog(null, "*Campo Requerido [Cantidad] -> Debe ingresar La Cantidad del Producto");
            txtCantidad.requestFocus();
            return;        
        }
        
        // Precio
        if(txtValor.getText().equals("")){
            JOptionPane.showMessageDialog(null, "*Campo Requerido [Precio] -> Debe ingresar el Precio del Producto");
            txtValor.requestFocus();
            return;        
        }
        
        // Cargo la tabla
        // Columnas (Titulos)
        if(contador == 0){
            modelo.addColumn("Producto");
            modelo.addColumn("Cantidad");
            modelo.addColumn("Precio");
            modelo.addColumn("SubTotal");   
            contador++;            
        }
        
        // Filas (productos)
        
        columna[0] = txtProducto.getText();
        columna[1] = txtCantidad.getText();
        columna[2] = txtValor.getText();
        
        // Calculo el valor (Total del la linea)
        SubtotalProducto = Integer.parseInt(txtCantidad.getText()) * Integer.parseInt(txtValor.getText());
        columna[3] = String.valueOf(SubtotalProducto);
        
        modelo.addRow(columna);
        
        // Limpiar los campos
        txtProducto.setText("");
        txtCantidad.setText("");
        txtValor.setText("");
        txtProducto.requestFocus();

    }//GEN-LAST:event_btnAdicionarActionPerformed

